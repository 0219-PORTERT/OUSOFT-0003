	TIM_OC_InitTypeDef sConfig;

	uint16_t Fpwm = 100;
	uint16_t psc = 200;

	uint16_t ARR = (uint16_t)0+(( (2*APB1_FREQ)/(Fpwm * (1+psc)) )-1);
	htim3.Init.Prescaler         = psc+1;
	htim3.Init.Period            = ARR;





	uint32_t pulse = (ARR * 50 )/100;


